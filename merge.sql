-- Data Combining 

DROP TABLE IF EXISTS `2023_Data.2023_merged`;

-- Get all 12 csv files into one.

CREATE TABLE IF NOT EXISTS `2023_Data.2023_merged` AS (
  SELECT * FROM `2023_Data.2023_01`
  UNION ALL
  SELECT * FROM `2023_Data.2023_02`
  UNION ALL
  SELECT * FROM `2023_Data.2023_03`
  UNION ALL
  SELECT * FROM `2023_Data.2023_04`
  UNION ALL
  SELECT * FROM `2023_Data.2023_05`
  UNION ALL
  SELECT * FROM `2023_Data.2023_06`
  UNION ALL
  SELECT * FROM `2023_Data.2023_07`
  UNION ALL
  SELECT * FROM `2023_Data.2023_08`
  UNION ALL
  SELECT * FROM `2023_Data.2023_09`
  UNION ALL
  SELECT * FROM `2023_Data.2023_10`
  UNION ALL
  SELECT * FROM `2023_Data.2023_11`
  UNION ALL
  SELECT * FROM `2023_Data.2023_12`
);
SELECT COUNT(*)
FROM `2023_Data.2023_merged`;
